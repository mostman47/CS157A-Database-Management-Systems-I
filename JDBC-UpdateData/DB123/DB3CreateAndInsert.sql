
DROP TABLE Movies;
CREATE TABLE Movies (
    title      VARCHAR(22), 
    year       INTEGER, 
    length     INTEGER, 
    inColor    CHAR(1), 
    studioName CHAR(60), 
    producerC# INTEGER, 
    PRIMARY KEY (title, year)
);


DROP TABLE MOVIEEXEC;
CREATE TABLE MovieExec (
    name     CHAR(30), 
    address  VARCHAR(50), 
    cert#    INTEGER PRIMARY KEY, 
    netWorth INTEGER
);

DROP TABLE MOVIESTAR;
CREATE TABLE MOVIESTAR(
	NAME      CHAR(30) NOT NULL, 
	ADDRESS   VARCHAR2(31), 
	GENDER    CHAR(6) NOT NULL, 
	BIRTHDATE INTEGER NOT NULL);

DROP TABLE STARSIN;
CREATE TABLE STARSIN(
	MOVIETITLE CHAR(22) NOT NULL, 
	MOVIEYEAR  INTEGER  NOT NULL, 
	STARNAME   CHAR(30) NOT NULL);

