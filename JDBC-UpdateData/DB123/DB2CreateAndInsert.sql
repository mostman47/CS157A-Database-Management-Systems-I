REMARK add a foreign key that references the EMPLOYEE table
ALTER TABLE DEPENDENT
ADD CONSTRAINT EMPLOYEE_FK_DEPENDENT
FOREIGN KEY (DEPRIKEY) REFERENCES EMPLOYEE (PRIKEY);

