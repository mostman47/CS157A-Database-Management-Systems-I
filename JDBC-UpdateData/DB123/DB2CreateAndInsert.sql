DROP TABLE EMPLOYEE;

REMARK Create the EMPLOYEE table
CREATE TABLE EMPLOYEE
(PRIKEY VARCHAR2(10) NOT NULL,
 FNAME VARCHAR2(10),
 MINIT VARCHAR2(1),
 LNAME VARCHAR2(10),
 SSN VARCHAR2(10),
 BDATE VARCHAR2(10),
 ADDRESS VARCHAR2(30),
 SEX VARCHAR2(1),
 SALARY NUMBER(20),
 SUPERPRIKEY VARCHAR2(12),
 SUPERSSN VARCHAR2(12),
 DNO NUMBER(8),
 EMPLOYEE_SEQ INT);

REMARK we're going to add a primary key
ALTER TABLE EMPLOYEE
ADD CONSTRAINT PK_EMPLOYEE
PRIMARY KEY (PRIKEY);

DROP TABLE DEPARTMENT;

REMARK Create the DEPARTMENT table
CREATE TABLE DEPARTMENT
(DNAME VARCHAR2(15),
 DNUMBER NUMBER(10) NOT NULL,
 MGRPRIKEY VARCHAR2(12),
 MGRSSN  VARCHAR2(12),
 MGRSTARTDATE VARCHAR2(10),
 DEPARTMENT_SEQ INT);

REMARK we're going to add a primary key
ALTER TABLE DEPARTMENT
ADD CONSTRAINT PK_DEPARTMENT
PRIMARY KEY (DNUMBER);

REMARK add a foreign key that references the EMPLOYEE table
ALTER TABLE DEPARTMENT
ADD CONSTRAINT EMPLOYEE_FK_DEPARTMENT
FOREIGN KEY (MGRPRIKEY) REFERENCES EMPLOYEE (PRIKEY);

DROP TABLE PROJECT;

REMARK Create the PROJECT table
